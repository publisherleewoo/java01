package com.javalec.ex;

public class Calculator {

	public void add(int f,int s) {
		 System.out.println("+");
		 System.out.println(f+s);
	}

	public void min(int f,int s) {
		 System.out.println("-");
		 System.out.println(f-s);
	}

	public void mul(int f,int s) {
		 System.out.println("*");
		 System.out.println(f*s);	
	}

	public void div(int f,int s) {
		 System.out.println("/");
		 System.out.println(f/s); 
	}

}
